const SUPPORTED = ['en', 'de', 'fr', 'it'];
const STORAGE_KEY = 'freett-lang';

let _lang = localStorage.getItem(STORAGE_KEY) || 'de';
if (!SUPPORTED.includes(_lang)) _lang = 'de';

function t(key, ...args) {
  const dict = TRANSLATIONS[_lang] || TRANSLATIONS.de;
  const val  = dict[key] ?? TRANSLATIONS.en[key] ?? key;
  return typeof val === 'function' ? val(...args) : val;
}

function setLang(code) {
  if (!SUPPORTED.includes(code)) return;
  _lang = code;
  localStorage.setItem(STORAGE_KEY, code);
}

function getLang() { return _lang; }

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
}

window.FREETT_I18N = { t, setLang, getLang, applyTranslations, SUPPORTED };

